// 확장표기 \"와 \'의 이용 예

class Quotation {
    
    public static void main(String[] args) {
        System.out.println("문자열 리터럴과 문자 상수에 대해서.");

        System.out.println("큰 따옴표 사이에 표기된 \"ABC\"는 문자열 리터럴입니다.");

        System.out.print("작은 따옴표 사이에 표기된 ");
        System.out.print('\'');
        System.out.println("A'는 문자 리터럴입니다.");
    }
}