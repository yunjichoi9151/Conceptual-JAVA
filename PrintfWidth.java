// 여러 가지 자릿수의 정수를 적어도 3자릿수로 표시

class PrintfWidth {
    
    public static void main(String[] args) {
        System.out.printf("%3d\n", 1);
        System.out.printf("%3d\n", 12);
        System.out.printf("%3d\n", 123);
        System.out.printf("%3d\n", 1234);
        System.out.printf("%3d\n", 12345);
    }
}
